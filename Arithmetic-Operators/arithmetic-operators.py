# + : Addition
# - : Subtraction
# * : Multiplication
# / : Division

print(7 / 2) # 3.5
print(-7 / 2) # -3.5

print(7/3) # 2.3333333333333335
print(-7/3) # -2.3333333333333335

print(8 / 3) # 2.6666666666666665
print(-8 / 3) # -2.6666666666666665
# // : Integer Division, divides and rounds down（無條件捨去） to the nearest integer

print(7 // 2) # 3
print(-7 // 2) # 4

print(7 // 3) # 2
print(-7 // 3) # -3

print(8 // 3) # 2
print(-8 // 3) # -3
# % : Mod (the remainder after dividing)

print(9 % 2) # 1
# ** : Exponentiation

print(3 ** 2) # 9

